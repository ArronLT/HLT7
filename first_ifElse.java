public class first_ifElse {

   public static void main(String[] args) {

      int ageJim = 30;
      int ageClare = 25;

      if (ageJim > ageClare) {
         System.out.println(" Jim is older than Clare");
      } else if (ageClare > ageJim) {
         System.out.println(" Clare is older than Jim");
      } else {
         System.out.println("Both Jim and Clare are the same age");
      }

   }
}
